public class CommonDAOFactory extends DaoFactory {

    /**
    * インスタンス化を禁止します。
    */
    private CommonDAOFactory() {
    }

    /**
    * DAOインスタンスを生成します。
    * @return DAOインスタンス
    */
    public static CommonDAO createCommonDAO() {
        return (CommonDAO) DaoFactory.createDao(CommonDAO.class);
    }
}
